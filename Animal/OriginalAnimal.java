package src;

public class Animal {


        protected int age;
      	
		
		
	protected int getAge() { return age; }
        protected void setAge(int age) { this.age = age; }

   
	protected void run() {
		System.out.println("run");
		
	}

	
	protected void walk() {
		System.out.println("walk");
		
	}

	
	protected void jump() {
		System.out.println("jump");
		
	}


	protected void stop() {
		System.out.println("stop");
		
	}


}

